import { factories } from "../../testHelpers/factories";
import { pointDistanceToNook } from "../nookUtils";

describe("pointDistanceToNook", () => {
  it("returns the correct distance from the center of the nook", () => {
    const point = { x: 5, y: 5 };
    const nook = factories.nook.build(undefined, {
      transient: {
        coords: [
          {
            x: 0,
            y: 0,
          },
          {
            x: 1,
            y: 0,
          },
        ],
      },
    });

    expect(pointDistanceToNook(point, nook).toFixed(2)).toEqual("6.73");
  });
});
