import { Nook } from "./GameMap";
import { Point } from "./Position";
import { calcCentroid, euclideanDistance } from "./positionUtils";

export const pointDistanceToNook = (point: Point, nook: Nook) => {
  const centroid = calcCentroid(nook.nookCoords.coords);
  return euclideanDistance(point, centroid);
};
